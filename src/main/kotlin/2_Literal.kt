fun main(args: Array<String>) {
//    mendeklarasi literal

//    literal pada tipe data int/integer/angka
    var intLiteral = 5

//    literal pada tipe data double/float (bilangan berkoma)
    var doubleLiteral = .02

//    literal pada tipe data string
    var stringLiteral = "Hello"

//    literal pada tipe data char/character
    var charLiteral = '1'

//    literal pada tipe data boolean
    var boolLiteral = true

//    mencetak nilai sesuai tipe data yang sudah di tentukan nilainya
    println("intLiteral = $intLiteral")
    println("doubleLiteral = $doubleLiteral")
    println("stringLiteral = $stringLiteral")
    println("charLiteral = $charLiteral")
    println("boolLiteral = $boolLiteral")
}